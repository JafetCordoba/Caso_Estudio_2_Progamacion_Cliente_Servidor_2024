    // Método para insertar un producto en la base de datos
    private void insertarProducto(String nombre, String descripcion, double precio, String codigo, String pasillo) {
        try {
            String query = "INSERT INTO productos (nombre, descripcion, precio, codigo, pasillo) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement pst = conn.prepareStatement(query);
            pst.setString(1, nombre);
            pst.setString(2, descripcion);
            pst.setDouble(3, precio);
            pst.setString(4, codigo);
            pst.setString(5, pasillo);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Producto insertado correctamente");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al insertar producto: " + ex.getMessage());
        }
    }

