    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
// Obtener la información del producto desde los campos de texto y el ComboBox
        String nombre = jTextField1.getText();
        String descripcion = jTextField2.getText();
        String precioStr = jTextField3.getText();
        String codigo = jTextField4.getText();
        String pasillo = (String) jComboBox1.getSelectedItem();

        // Validación de campos
        if (nombre.isEmpty() || precioStr.isEmpty() || codigo.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, complete todos los campos obligatorios.");
            return;
        }

        // Convertir el precio a double
        double precio = 0.0;
        try {
            precio = Double.parseDouble(precioStr);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "El precio debe ser un número válido.");
            return;
        }

        // Llamar a la función insertarProducto con los datos obtenidos
        insertarProducto(nombre, descripcion, precio, codigo, pasillo);

    }//GEN-LAST:event_jButton3ActionPerformed

